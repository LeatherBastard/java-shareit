DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS items CASCADE;
DROP TABLE IF EXISTS requests CASCADE;
DROP TABLE IF EXISTS bookings CASCADE;

CREATE TABLE users(
    user_id INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(20) NOT NULL,
    email VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE items(
    item_id INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    description VARCHAR(200),
    available BOOLEAN NOT NULL,
    owner_id INTEGER REFERENCES users(user_id) ON DELETE CASCADE,
    request_id INTEGER
);

CREATE TABLE requests(
    request_id INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    description VARCHAR(50) NOT NULL,
    requestor_id INTEGER REFERENCES users(user_id) ON DELETE CASCADE,
    created TIMESTAMP NOT NULL
);

CREATE TABLE bookings(
    booking_id INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    start_date TIMESTAMP NOT NULL,
    end_date TIMESTAMP NOT NULL,
    item_id INTEGER REFERENCES items(item_id) ON DELETE CASCADE,
    booker_id INTEGER REFERENCES users(user_id) ON DELETE CASCADE,
    status VARCHAR(8) NOT NULL
);
